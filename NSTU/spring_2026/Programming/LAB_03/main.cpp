#include <iostream>
#include "people.h"

int main(){
    // Подготовка дат рождения
    date date1980 = {15, 4, 1980};
    date date1982 = {22, 11, 1982};
    date date2015 = {5, 6, 2015};

    // ==========================================
    // ТЕСТ 1: Создание корректных родителей
    // ==========================================
    std::cout << "[Тест 1] Создание родителей..." << std::endl;
    
    people father("Иванов Иван Петрович", 46, nullptr, nullptr, Passport, Male, 4510, 123456, "Москва", date1980);
    people mother("Иванова Ольга Сидоровна", 44, nullptr, nullptr, Passport, Female, 4512, 654321, "Питер", date1982);
    
    std::cout << "Отец: " << father.getFIO() << ", Пол: " << (father.getGender() == Male ? "Муж" : "Жен") << std::endl;
    std::cout << "Мать: " << mother.getFIO() << ", Пол: " << (mother.getGender() == Female ? "Жен" : "Муж") << std::endl;
    std::cout << "------------------------------------------" << std::endl;

    // ==========================================
    // ТЕСТ 2: Проверка валидации возраста для паспорта (Ребенку 11 лет)
    // ==========================================
    std::cout << "[Тест 2] Попытка выдать паспорт 11-летнему ребенку в конструкторе:" << std::endl;
    
    // Передаем в конструктор тип 'Passport', но ребенку 11 лет. Должно сработать предупреждение.
    people child("Иванов Алексей Иванович", 11, &mother, &father, Passport, Male, 1020, 111222, "Новгород", date2015);
    
    std::cout << "Фактический тип документа у ребенка: " 
              << (child.getDocType() == Passport ? "Паспорт" : "Свидетельство о рождении") << std::endl;
    std::cout << "------------------------------------------" << std::endl;

    // ==========================================
    // ТЕСТ 3: Нарушение правил родственных связей
    // ==========================================
    std::cout << "[Тест 3] Проверка защиты от некорректных родителей:" << std::endl;

    // 3.1. Пытаемся назначить отца матерью
    std::cout << "Пробуем назначить отца (мужчину) в качестве матери: " << std::endl;
    child.setMother(&father); 

    // 3.2. Пытаемся назначить человека родителем самого себя
    std::cout << "Пробуем назначить человека родителем самого себя: " << std::endl;
    father.setFather(&father); 
    
    std::cout << "------------------------------------------" << std::endl;

    // ==========================================
    // ТЕСТ 4: Проверка истории паспорта (Запрет отката на свидетельство)
    // ==========================================
    std::cout << "[Тест 4] Проверка отката типа документа:" << std::endl;
    
    std::cout << "Текущий документ отца: " << (father.getDocType() == Passport ? "Паспорт" : "Свидетельство") << std::endl;
    std::cout << "Изменение паспорта отца обратно на Свидетельство о рождении: " << std::endl;
    father.setDocType(Birth_sertificate); // Должна быть ошибка
    
    std::cout << "------------------------------------------" << std::endl;

    // ==========================================
    // ТЕСТ 5: Изменение ФИО и количества детей через сеттеры
    // ==========================================
    std::cout << "[Тест 5] Проверка геттеров:" << std::endl;
    
    std::cout << "Старое ФИО ребенка: " << child.getFIO() << std::endl;
    child.setFIO("Иванов-Суворов Алексей Иванович");
    std::cout << "Новое ФИО ребенка: " << child.getFIO() << std::endl;

    father.setChildrenCounter(1);
    std::cout << "Количество детей у отца: " << father.getChildrenCounter() << std::endl;
    

    return 0;
}